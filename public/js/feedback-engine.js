function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function normalizeText(value) {
  return (value || "")
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function tokenize(value) {
  return new Set(normalizeText(value).split(" ").filter((token) => token.length > 2));
}

function similarityScore(left, right) {
  const leftTokens = tokenize(left);
  const rightTokens = tokenize(right);

  if (!leftTokens.size || !rightTokens.size) {
    return 0;
  }

  let overlap = 0;
  leftTokens.forEach((token) => {
    if (rightTokens.has(token)) {
      overlap += 1;
    }
  });

  return overlap / Math.max(leftTokens.size, rightTokens.size);
}

function chooseRandomEntry(options, usedIndexes, rng) {
  const available = options
    .map((entry, index) => ({ entry, index }))
    .filter(({ index }) => !usedIndexes.has(index));

  const pool = available.length ? available : options.map((entry, index) => ({ entry, index }));
  const choice = pool[Math.floor(rng() * pool.length)];
  usedIndexes.add(choice.index);
  return choice;
}

function createSeededRng(seedInput) {
  let seed = 0;
  const seedText = String(seedInput);
  for (let index = 0; index < seedText.length; index += 1) {
    seed = (seed * 31 + seedText.charCodeAt(index)) >>> 0;
  }

  return () => {
    seed = (seed * 1664525 + 1013904223) >>> 0;
    return seed / 4294967296;
  };
}

function extractKeywords(text, limit = 4) {
  const stopWords = new Set([
    "about", "after", "again", "almost", "along", "also", "because", "being", "between",
    "could", "every", "feels", "from", "going", "into", "just", "like", "make", "maybe",
    "more", "most", "only", "over", "really", "sound", "still", "that", "their", "them",
    "there", "these", "they", "this", "through", "track", "trying", "what", "when", "with",
    "your"
  ]);

  const counts = new Map();
  normalizeText(text)
    .split(" ")
    .filter((token) => token.length > 3 && !stopWords.has(token))
    .forEach((token) => {
      counts.set(token, (counts.get(token) || 0) + 1);
    });

  return [...counts.entries()]
    .sort((left, right) => right[1] - left[1])
    .slice(0, limit)
    .map(([token]) => token);
}

function buildContextPhrases(data, audioFeatures) {
  const phrases = [];
  const keywords = unique([
    ...extractKeywords(data.inspiration, 2),
    ...extractKeywords(data.identity, 2),
    ...extractKeywords(data.inspirations, 2),
  ]).slice(0, 4);

  if (keywords.length) {
    phrases.push(`The language around ${keywords.join(", ")} is already shaping how the session reads emotionally.`);
  }

  if (data.fileName && data.fileName !== "Untitled idea") {
    const cleanFileName = data.fileName.replace(/\.[^/.]+$/, "");
    phrases.push(`Even the file title "${cleanFileName}" points toward a specific emotional frame for the track.`);
  }

  if (audioFeatures?.durationSeconds) {
    if (audioFeatures.durationSeconds < 70) {
      phrases.push("The shorter runtime gives the idea a focused, snapshot-like energy instead of asking it to say everything at once.");
    } else if (audioFeatures.durationSeconds > 220) {
      phrases.push("The longer runtime gives the arrangement room to develop atmosphere and contrast more gradually.");
    } else {
      phrases.push("The runtime feels balanced enough to let the idea breathe without losing focus.");
    }
  }

  if (audioFeatures?.dynamicProfile === "wide") {
    phrases.push("The waveform suggests a healthy amount of rise and fall, which helps the emotional shape feel alive.");
  } else if (audioFeatures?.dynamicProfile === "steady") {
    phrases.push("The waveform stays relatively steady, so contrast moments will matter even more if you want the arc to feel bigger.");
  }

  if (audioFeatures?.brightnessProfile === "bright") {
    phrases.push("The brighter upper detail in the audio readout helps the track feel open and present.");
  } else if (audioFeatures?.brightnessProfile === "dark") {
    phrases.push("The darker tonal balance reinforces the mood and keeps the weight centered lower in the frame.");
  }

  if (audioFeatures?.transientProfile === "punchy") {
    phrases.push("The transients suggest the drums are helping define the movement instead of sitting passively underneath it.");
  } else if (audioFeatures?.transientProfile === "soft") {
    phrases.push("The softer transient shape supports immersion and mood more than sharp attack.");
  }

  return phrases;
}

function buildAudioFeatures(data) {
  const audioFeatures = data.audioFeatures || {};
  const fallbackBars = Array.isArray(audioFeatures.waveformBars) ? audioFeatures.waveformBars : [];
  const averageBar = fallbackBars.length
    ? fallbackBars.reduce((sum, value) => sum + value, 0) / fallbackBars.length
    : 58;
  const dynamicSpread = fallbackBars.length
    ? Math.max(...fallbackBars) - Math.min(...fallbackBars)
    : 26;

  return {
    durationSeconds: audioFeatures.durationSeconds || 0,
    sampleRate: audioFeatures.sampleRate || 0,
    channelCount: audioFeatures.channelCount || 1,
    averageLevel: audioFeatures.averageLevel || averageBar / 100,
    peakLevel: audioFeatures.peakLevel || clamp((averageBar + dynamicSpread) / 100, 0.3, 1),
    dynamicSpread,
    waveformBars: fallbackBars,
    brightness: audioFeatures.brightness || clamp((audioFeatures.spectralBalance || 0.5), 0, 1),
    zeroCrossingRate: audioFeatures.zeroCrossingRate || clamp(dynamicSpread / 100, 0.08, 0.55),
  };
}

function deriveAudioDescriptors(audioFeatures) {
  const dynamicProfile = audioFeatures.dynamicSpread >= 42
    ? "wide"
    : audioFeatures.dynamicSpread <= 22
      ? "steady"
      : "balanced";

  const brightnessProfile = audioFeatures.brightness >= 0.62
    ? "bright"
    : audioFeatures.brightness <= 0.4
      ? "dark"
      : "balanced";

  const transientProfile = audioFeatures.zeroCrossingRate >= 0.24 || audioFeatures.peakLevel - audioFeatures.averageLevel >= 0.24
    ? "punchy"
    : audioFeatures.zeroCrossingRate <= 0.12
      ? "soft"
      : "controlled";

  return {
    ...audioFeatures,
    dynamicProfile,
    brightnessProfile,
    transientProfile,
  };
}

function createEnergyBars(audioFeatures, primaryMood, selectedMoods, stage) {
  const moodBoost = selectedMoods.length * 2;
  const base = clamp(34 + Math.round(audioFeatures.averageLevel * 34), 24, 76);
  const spreadBoost = clamp(Math.round(audioFeatures.dynamicSpread / 2), 10, 26);
  const brightnessBoost = Math.round(audioFeatures.brightness * 18);
  const aggressionBoost = primaryMood === "aggressive" ? 16 : 0;
  const atmosphereBoost = ["cinematic", "dreamy", "atmospheric", "dark"].includes(primaryMood) ? 8 : 0;
  const stageBoost = stage === "polished" ? 4 : stage === "experimental" ? 10 : 7;

  return Array.from({ length: 12 }, (_, index) => {
    const wave = Math.sin((index + 1) * 0.82) * 10;
    const alt = Math.cos((index + 2) * 0.54) * 8;
    return clamp(
      base + spreadBoost + brightnessBoost + aggressionBoost + atmosphereBoost + stageBoost + moodBoost + wave + alt,
      18,
      96
    );
  });
}

function buildIdentityTraits(data, primaryProfile, stageProfile, audioFeatures, contextPhrases, rng) {
  const baseTraits = [
    ...primaryProfile.traits,
    ...data.selectedMoods.filter((entry) => entry !== data.primaryMood),
    stageProfile.trait,
  ];

  if (audioFeatures.dynamicProfile === "wide") {
    baseTraits.push("dynamic contrast");
  }
  if (audioFeatures.dynamicProfile === "steady") {
    baseTraits.push("steady emotional line");
  }
  if (audioFeatures.brightnessProfile === "dark") {
    baseTraits.push("tonal shadow");
  }
  if (audioFeatures.brightnessProfile === "bright") {
    baseTraits.push("bright lift");
  }
  if (audioFeatures.transientProfile === "punchy") {
    baseTraits.push("drum definition");
  }
  if (audioFeatures.transientProfile === "soft") {
    baseTraits.push("soft attack");
  }
  if (data.identity.trim()) {
    baseTraits.push("self-described intent");
  }
  if (data.inspirations.trim()) {
    baseTraits.push("influence-aware");
  }
  if (contextPhrases.some((entry) => entry.includes("file title"))) {
    baseTraits.push("clear framing");
  }

  return unique(baseTraits)
    .sort(() => rng() - 0.5)
    .slice(0, clamp(3 + Math.round(rng() * 3), 3, 6));
}

function combineThought(base, addon) {
  return addon ? `${base} ${addon}` : base;
}

function buildResponseText(result) {
  return normalizeText([
    result.vibeSummary,
    result.arrangementInsight,
    result.identityInsight,
    result.producerInsight,
    result.nextStepSuggestion,
    ...(result.traits || []),
  ].join(" "));
}

export function avoidRecentRepeats(newResponse, previousSessions = []) {
  const recentSessions = previousSessions.slice(0, 3);
  const comparisons = recentSessions.map((session) => {
    const previousText = buildResponseText({
      vibeSummary: session.analysisResults?.vibeSummary || session.vibeSummary || "",
      arrangementInsight: session.analysisResults?.arrangementInsight || session.arrangementInsight || "",
      identityInsight: session.analysisResults?.artistIdentityInsight || session.artistIdentityInsight || "",
      producerInsight: session.analysisResults?.producerInsight || session.producerInsight || "",
      nextStepSuggestion: session.analysisResults?.nextStepSuggestion || session.nextStepSuggestion || "",
      traits: session.analysisResults?.identityTraits || session.identityTraits || [],
    });

    return similarityScore(newResponse, previousText);
  });

  const highestSimilarity = comparisons.length ? Math.max(...comparisons) : 0;
  return {
    shouldRegenerate: highestSimilarity >= 0.64,
    highestSimilarity,
    comparisons,
  };
}

